package edu.upf.taln.voice.modulator;

public class ModulatorException extends Exception {

	public ModulatorException() {
		super();
	}

	public ModulatorException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ModulatorException(String message, Throwable cause) {
		super(message, cause);
	}

	public ModulatorException(String message) {
		super(message);
	}

	public ModulatorException(Throwable cause) {
		super(cause);
	}
}
